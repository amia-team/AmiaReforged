namespace AmiaReforged.PwEngine.Features.Codex.Domain.Enums;

/// <summary>
/// Represents the rarity/importance tier of a lore entry.
/// </summary>
public enum LoreTier
{
    /// <summary>
    /// Common knowledge, widely known information.
    /// </summary>
    Common = 0,

    /// <summary>
    /// Uncommon knowledge, requires some exploration to discover.
    /// </summary>
    Uncommon = 1,

    /// <summary>
    /// Rare knowledge, difficult to find or restricted.
    /// </summary>
    Rare = 2,

    /// <summary>
    /// Legendary knowledge, extremely rare or secret information.
    /// </summary>
    Legendary = 3
}
